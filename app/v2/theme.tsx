"use client";

import { useTheme } from "next-themes";
import { useEffect, useState } from "react";

type ThemeType = "system" | "dark" | "light";
export type IconsType = "github" | "linkedin" | "x" | "leetcode" | "medium";
export function ModeToggle() {
  const [themeCounter, setThemeCounter] = useState(0);
  const [btnTheme, setBtnTheme] = useState<ThemeType>("system");
  const { setTheme } = useTheme();

  useEffect(() => {
    if (themeCounter === 0) {
      setTheme("system");
      setBtnTheme("system");
    } else if (themeCounter === 1) {
      setTheme("dark");
      setBtnTheme("dark");
    } else {
      setTheme("light");
      setBtnTheme("light");
    }
  }, [themeCounter]);
  return (
    <>
      <button
        onClick={() => {
          setThemeCounter((themeCounter + 1) % 3);
        }}
        aria-label="theme-btn"
      >
        <div className="flex p-2 justify-center">
          <ThemeIcon theme={btnTheme} />
        </div>
      </button>
    </>
  );
}

